function displayMap() {
    gameContainer.innerHTML = "";
    map.forEach((el) => {
        let line = document.createElement('div');
        line.classList.add("line")
        gameContainer.appendChild(line);
        el.forEach((value, index) => {
            let cell = document.createElement('div');
            cell.classList.add("cell")
            line.appendChild(cell);
            switch (value) {
                case 0:
                    cell.style.backgroundColor = "rgb(159, 205, 234))";
                    break;
                case 1:
                    cell.style.backgroundColor = "Black";
                    break;
            }
            cell.addEventListener("click", () => {
                if (value == 0) {
                    el.splice(index, 1, 1)
                    displayMap()
                }
                else {
                    el.splice(index, 1, 0)
                    displayMap()
                }
            })
        })
    })
}
function autoplay() {
    myInterval = setInterval(() => {
        rules()
    }, 170)
}
function rules() {
    let alive = [];
    let dead = [];
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[i].length; j++) {
            if (map[i][j] == 0) {
                let sum = somme(map, i, j);
                if (sum === 3) {
                    let arrAlive = [];
                    arrAlive.push(i);
                    arrAlive.push(j);
                    alive.push(arrAlive);
                }
            }
            if (map[i][j] == 1) {
                sum = somme(map, i, j);
                if (sum < 2 || sum > 3) {
                    let arrDead = [];
                    arrDead.push(i);
                    arrDead.push(j);
                    dead.push(arrDead);
                }
            }
        }
    }
    alive.forEach((el) => {
        remove(el[0], el[1], 1)
    })

    dead.forEach((el) => {
        remove(el[0], el[1], 0)
    })
    displayMap()
}

function somme(array, indLine, indCell) {
    let resulat = 0;
    if (array[indLine - 1]) {
        if (array[indLine - 1][indCell]) {
            resulat += array[indLine - 1][indCell];
        }
        if (array[indLine - 1][indCell - 1]) {
            resulat += array[indLine - 1][indCell - 1];
        }
        if (array[indLine - 1][indCell + 1]) {
            resulat += array[indLine - 1][indCell + 1];
        }
    }
    if (array[indLine + 1]) {
        if (array[indLine + 1][indCell]) {
            resulat += array[indLine + 1][indCell];
        }
        if (array[indLine + 1][indCell - 1]) {
            resulat += array[indLine + 1][indCell - 1];
        }
        if (array[indLine + 1][indCell + 1]) {
            resulat += array[indLine + 1][indCell + 1];
        }
    }
    if (array[indLine][indCell - 1]) {
        resulat += array[indLine][indCell - 1];
    }
    if (array[indLine][indCell + 1]) {
        resulat += array[indLine][indCell + 1];
    }
    return resulat
}

